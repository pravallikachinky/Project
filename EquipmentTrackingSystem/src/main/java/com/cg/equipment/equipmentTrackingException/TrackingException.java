package com.cg.equipment.equipmentTrackingException;

public class TrackingException extends Exception {

	public TrackingException() {
		super();
	}

	public TrackingException(String message) {
		super(message);
		
	}

	public TrackingException(Throwable arg0) {
		super(arg0);
		
	}

	public TrackingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public TrackingException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
			}

}
